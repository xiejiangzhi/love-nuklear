
function love.conf(t)
  t.identity = 'LoveUI'
  t.title = "LoveUI"
  t.version = "11.3"

  t.window.width = 1440
  t.window.height = 900
  t.window.fullscreen = false

  t.window.vsync = false
  t.window.highdpi = true

  t.window.resizable = true
  t.window.minwidth = 800
  t.window.minheight = 600

  t.console = true
end

